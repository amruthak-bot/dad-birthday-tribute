const chapterList = document.getElementById("chapterList");
const escapeHTML = value => value.replace(/[&<>'"]/g, char => ({"&":"&amp;","<":"&lt;",">":"&gt;","'":"&#39;",'"':"&quot;"}[char]));

chapters.forEach((chapter, chapterIndex) => {
  const article = document.createElement("article");
  article.className = "chapter";
  const photos = chapter.photos.map((photo, index) => `
    <button class="memory-card reveal ${index === 0 ? "memory-wide" : ""}" data-src="images/${encodeURI(photo[0])}" data-caption="${escapeHTML(photo[1])}" aria-label="Open photograph: ${escapeHTML(photo[1])}">
      <img src="images/${encodeURI(photo[0])}" alt="${escapeHTML(photo[1])}" loading="lazy">
      <span><small>0${chapterIndex + 1}.${index + 1}</small>${escapeHTML(photo[1])}</span>
    </button>`).join("");
  article.innerHTML = `
    <div class="chapter-intro reveal"><span>${chapter.number}</span><div><p>${chapter.subtitle}</p><h3>${chapter.title}</h3><p>${chapter.text}</p></div></div>
    <div class="memory-grid">${photos}</div>`;
  chapterList.appendChild(article);
});

const lightbox = document.createElement("div");
lightbox.className = "lightbox";
lightbox.setAttribute("aria-hidden", "true");
lightbox.innerHTML = `<button aria-label="Close photograph">×</button><figure><img alt=""><figcaption></figcaption></figure>`;
document.body.appendChild(lightbox);

document.addEventListener("click", event => {
  const card = event.target.closest(".memory-card");
  if (!card) return;
  lightbox.querySelector("img").src = card.dataset.src;
  lightbox.querySelector("figcaption").textContent = card.dataset.caption;
  lightbox.classList.add("is-open");
  lightbox.setAttribute("aria-hidden", "false");
  document.body.classList.add("no-scroll");
});

const closeLightbox = () => {
  lightbox.classList.remove("is-open");
  lightbox.setAttribute("aria-hidden", "true");
  document.body.classList.remove("no-scroll");
};
lightbox.addEventListener("click", event => { if (event.target === lightbox || event.target.closest("button")) closeLightbox(); });
document.addEventListener("keydown", event => { if (event.key === "Escape") closeLightbox(); });

