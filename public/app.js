const ctx = canvas.getContext("2d");
let confetti = [];

function launchConfetti() {
  const rect = canvas.getBoundingClientRect();
  canvas.width = rect.width * devicePixelRatio; canvas.height = rect.height * devicePixelRatio;
  ctx.scale(devicePixelRatio, devicePixelRatio);
  confetti = Array.from({length: 150}, () => ({x: rect.width / 2, y: rect.height * .5, vx: (Math.random() - .5) * 13, vy: -Math.random() * 11 - 4, r: Math.random() * 5 + 2, color: ["#d8b66c", "#fff1d3", "#8f314b", "#e0a6a7"][Math.floor(Math.random()*4)], spin: Math.random()*6}));
  let frame = 0;
  function draw() {
    ctx.clearRect(0,0,rect.width,rect.height);
    confetti.forEach(p => { p.x += p.vx; p.y += p.vy; p.vy += .22; p.spin += .15; ctx.save(); ctx.translate(p.x,p.y); ctx.rotate(p.spin); ctx.fillStyle=p.color; ctx.fillRect(-p.r,-p.r/2,p.r*2,p.r); ctx.restore(); });
    if (frame++ < 220) requestAnimationFrame(draw); else ctx.clearRect(0,0,rect.width,rect.height);
  }
  draw();
}

